// Функция для запроса имени и отображения приветствия
const askName = () => {
  // Запрашиваем имя у пользователя
  const name = prompt("Введите ваше имя:");
  // Если имя не пустое, показываем приветствие
  if (name && name.trim() !== "") {
    alert(`Привет, ${name}!`);
  } else {
    alert("Пожалуйста, введите ваше имя.");
  }
};

// Вызов функции
askName();
